function [MultQ] = Quat_Mult(A,B)
    MultQ(1,1) = A(1,1)*B(1,1) - A(1,2)*B(1,2) + A(1,3)*B(1,3) - A(1,4)*B(1,4);
    MultQ(2,1) = A(1,1)*B(1,2) - A(1,2)*B(1,1) + A(1,3)*B(1,4) - A(1,4)*B(1,3);
    MultQ(3,1) = A(1,1)*B(1,3) - A(1,2)*B(1,4) + A(1,3)*B(1,1) - A(1,4)*B(1,2);
    MultQ(4,1) = A(1,1)*B(1,4) - A(1,2)*B(1,3) + A(1,3)*B(1,2) - A(1,4)*B(1,1);
end